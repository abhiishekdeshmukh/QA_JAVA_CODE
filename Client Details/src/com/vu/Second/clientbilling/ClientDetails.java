package com.vu.Second.clientbilling;

public class ClientDetails 
{
	
	public static void main(String[] args) 
	{
		//byte = 2 digits
		byte age = 24;
		System.out.println("The age is: "+ age);
		System.out.println(age);
		System.out.println("age");
		
		//short = 4 digits
		short emp = 1234;
		System.out.println("Emp ID is: " +emp);
		
		//int - 9 digits
		int regNo = 123456789;
		System.out.println("Reg No. is: " + regNo);
		
		//long - >9 digits
		long phone = 7020869957l;
		System.out.println("Phone number is" + phone);
		
		//float
		float avg = 98.67f;
		System.out.println("Avg is " + avg);
		
		//double
		double salary = 86759.8754;
	    System.out.println("Salary is: " +salary);
		
		//char
		char blockName = 'c';
		System.out.println("Block Name is: " + blockName);
		
		//boolean
		boolean empStatus = true;
		System.out.println("Emp Status is:" + empStatus);
		
		//String
		String email = "abhi@gmail.com";
		System.out.println("Mail ID is " + email);
	}
}
